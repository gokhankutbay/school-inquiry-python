age = int(input("Enter the person's age:: "))

if age<=5:
    print("Nursery.")
elif age==6:
    print("Kindergarten student.")
elif 7<=age<=10:
    print("Primary school student.")
elif 10<age<=14:
    print("Middle school student.")
elif 14<age<=18:
    print("High school student.")
elif age>18:
    print("University student/Graduate.")

